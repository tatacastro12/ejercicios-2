public class Main5 {

    public static void main(String[] args) {
        var estacion = "Otoño";

        switch (estacion) {
            case "Invierno":
                System.out.println("Invierno");
                break;

            case "Verano":
                System.out.println("Verano");
                break;

            case "Otoño":
                System.out.println("Otoño");
                break;

            case "Primavera":
                System.out.println("Primavera");
                break;

            default:
                System.out.println("no  tenemos estacion");
                break;
        }

    }
}